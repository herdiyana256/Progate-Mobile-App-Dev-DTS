//Variable dapat digunakan seperti string dan integer yang sudah kita pelajari. Nilai yang diberikan ke variable dapat digabungkan dengan string lain atau digunakan dalam kalkulasi dengan integer lain.

let name = 'Herdiyan';
console.log("Mr." + name);

let number = 25;
