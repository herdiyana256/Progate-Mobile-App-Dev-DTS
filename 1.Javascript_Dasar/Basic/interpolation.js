// String Interpolation (template literal)

//Template literal memungkinkan Anda untuk memasukkan variabel atau ekspresi langsung ke dalam string menggunakan backtick dan kurung kurawal ${ }.

const name = "Gundam";
const age = 1996;

const greeting = `Hello, my name is ${name} and i am ${age} years old.`;

console.log(greeting);

//Hello, my name is Gundam and i am 1996 years old.
