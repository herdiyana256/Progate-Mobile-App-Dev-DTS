// Template Literal di Javascript 

//Template literal adalah fitur yang diperkenalkan di ES6 (ECMAScript 2015) yang memungkinkan penulisan string dengan cara yang lebih fleksibel dan mudah dibaca.


//String Multiline
// Dengan template literal, Anda dapat membuat string multiline dengan mudah tanpa perlu menggunakan karakter escape \n


const message = `this is a multiline string
It can span multiple lines
without any problems.`;
console.log(message);

/*
this is a multiline string
It can span multiple lines
without any problems.
*/